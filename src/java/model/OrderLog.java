package model;

import java.util.Date;

/**
 *
 * @author Benjamin
 */
public class OrderLog {

    private int id;
    private Date date;
    private String name;

    public OrderLog() {
    }

    public OrderLog(int id, Date date, String name) {
        this.id = id;
        this.date = date;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
