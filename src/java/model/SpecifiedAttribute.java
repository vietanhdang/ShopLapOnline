package model;

import java.util.List;
import java.util.Objects;

/**
 *
 * @author vietd
 */
public class SpecifiedAttribute extends AbstractModel {

    private String name;
    private String filterName;

    public SpecifiedAttribute(String name, int id, String filterName) {
        super(id);
        this.name = name;
        this.filterName = filterName;
    }

    public String getFilterName() {
        return filterName;
    }

    public void setFilterName(String filterName) {
        this.filterName = filterName;
    }

    public SpecifiedAttribute() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 59 * hash + Objects.hashCode(this.id);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final SpecifiedAttribute other = (SpecifiedAttribute) obj;
        return Objects.equals(this.id, other.id);
    }

}
